        j1 = i1 + half; j2 = i2 - half
        for i in j1:j2
            s = cent[13] * a[i]
            for j in 1:12; s += cent[13 - j] * a[i - j] + cent[13 + j] * a[i + j]; end
            b[i] = s
        end
        for layer in 1:6
            l1 = j1 - layer; l2 = j2 + layer
            for k in 1:24
                m1 = l1 + layer + 12 - k; m2 = l2 - layer - 12 + k
                b[l1] += endw[k, layer] * a[m1]
                b[l2] += endw[k, layer] * a[m2]
            end
        end
    else
        half = 4; cent = _X13_QCENT; endw = _X13_ENDW_QUARTERLY
        j1 = i1 + half; j2 = i2 - half
        for i in j1:j2
            s = cent[5] * a[i]
            for j in 1:4; s += cent[5 - j] * a[i - j] + cent[5 + j] * a[i + j]; end
            b[i] = s
        end
        for layer in 1:2
            l1 = j1 - layer; l2 = j2 + layer
            for k in 1:8
