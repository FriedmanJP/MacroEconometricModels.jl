# ─────────────────────────────────────────────────────────────────────────────
# Field-value flattening (struct → plain values)
# ─────────────────────────────────────────────────────────────────────────────

# A value whose element type is one of these needs no flattening — JLD2 stores
# numbers/strings/symbols/bools/chars/enums (and arrays/dicts of them) robustly.
_is_plain_eltype(::Type{S}) where {S} = S <: Union{Number,AbstractString,Symbol,Char,Bool,Enum}

# Is `x` a MacroEconometricModels struct we should flatten recursively? Types
# from Base / LinearAlgebra / Distributions (parentmodule ≠ this package) fall
# through to JLD2's own storage; the manifest and covariance estimators have
# their own tagged-dict methods below and never reach here.
function _is_mem_struct(x)
    T = typeof(x)
    isstructtype(T) || return false
    return parentmodule(T) === MacroEconometricModels
end

# The manifest and LP covariance estimators are flattened by dedicated methods;
# everything else routes through the generic recursive `_ser_field(x)`.
_ser_field(x::ReproManifest) = _manifest_to_dict(x)
_ser_field(x::AbstractCovarianceEstimator) = _cov_to_dict(x)

function _ser_field(x)
    x === nothing && return nothing
    x isa Missing && return missing
    # Compiled closures/functions do not round-trip through a version-stable
    # payload. The only function field among registered types is the OPTIONAL
    # `StateSpaceModel.builder` re-fit helper, which reloads as `nothing`.
    x isa Function && return nothing
    x isa Enum && return x
    (x isa Number || x isa AbstractString || x isa Symbol || x isa Char) && return x
    if x isa AbstractArray
        _is_plain_eltype(eltype(x)) && return x
        return map(_ser_field, x)
    end
    x isa Tuple && return map(_ser_field, x)
    if x isa AbstractDict
        _is_plain_eltype(valtype(x)) && return x
        return Dict(k => _ser_field(v) for (k, v) in x)
    end
    _is_mem_struct(x) && return _struct_to_dict(x)
    return x
end

# Generic public-field capture: {fieldname => flattened value} for every field.
function _capture_fields(m)
    d = Dict{String,Any}()
    for f in fieldnames(typeof(m))
        d[String(f)] = _ser_field(getfield(m, f))
    end
    return d
end

# A nested struct is captured like the top-level payload, plus a `__struct__`
# tag so `_deser_field` can resolve its concrete type on the way back in.
function _struct_to_dict(m)
    d = _capture_fields(m)
    d["__struct__"] = String(nameof(typeof(m)))
    return d
end

_extract_manifest(m) = hasfield(typeof(m), :manifest) ? getfield(m, :manifest) : nothing

# LP covariance estimator ↔ dict. These are small config structs (bandwidth,
# kernel, prewhiten) — capture the type name + fields, rebuild by name.
function _cov_to_dict(c::AbstractCovarianceEstimator)
    d = Dict{String,Any}("__estimator__" => string(nameof(typeof(c))))
    for f in fieldnames(typeof(c))
        d[String(f)] = getfield(c, f)
    end
    return d
end

function _cov_from_dict(d::AbstractDict)
    name = String(d["__estimator__"])
    if name == "NeweyWestEstimator"
        return NeweyWestEstimator{Float64}(Int(d["bandwidth"]), _as_symbol(d["kernel"]), Bool(d["prewhiten"]))
    elseif name == "WhiteEstimator"
        return WhiteEstimator()
    elseif name == "DriscollKraayEstimator"
        return DriscollKraayEstimator{Float64}(Int(d["bandwidth"]), _as_symbol(d["kernel"]))
    end
    throw(SerializationError("unknown covariance estimator '$name' in serialized LPModel"))
end

# ─────────────────────────────────────────────────────────────────────────────
# Field-value reconstruction (plain values → struct)
# ─────────────────────────────────────────────────────────────────────────────

_as_symbol(x::Symbol) = x
_as_symbol(x::AbstractString) = Symbol(x)

# Inverse of `_ser_field`: rebuild manifests / covariance estimators / nested
# structs from their tagged dicts; recurse into arrays/dicts of them.
function _deser_field(x)
    if x isa AbstractDict
        haskey(x, "__manifest__")  && return _manifest_from_dict(x)
        haskey(x, "__estimator__") && return _cov_from_dict(x)
        haskey(x, "__struct__")    && return _deser_struct(x)
        _is_plain_eltype(valtype(x)) && return x
        return Dict(k => _deser_field(v) for (k, v) in x)
    end
    if x isa AbstractArray
        _is_plain_eltype(eltype(x)) && return x
        return map(_deser_field, x)
    end
    x isa Tuple && return map(_deser_field, x)
    return x
end

# Resolve a nested `__struct__` tag to its concrete type. Nested types need no
# `_SERIALIZABLE_TYPES` entry — they are looked up by name in the package module.
function _resolve_ser_type(name::AbstractString)
    sym = Symbol(name)
    isdefined(MacroEconometricModels, sym) || throw(SerializationError(
        "serialized nested type '$name' is not defined in this build of MacroEconometricModels"))
    T = getfield(MacroEconometricModels, sym)
    T isa Type || throw(SerializationError("serialized type tag '$name' does not name a type"))
    return T
end

_deser_struct(d::AbstractDict) =
    _from_serializable(_resolve_ser_type(String(d["__struct__"])), d, SERIALIZATION_FORMAT_VERSION)

# Infer a `T<:AbstractFloat` type parameter from the reconstructed field values
# (the first float scalar/array wins). Returns `nothing` for non-float or
# non-parametric types, in which case the un-parameterized constructor is used.
function _infer_float_param(args)
    for a in args
        a isa AbstractFloat && return typeof(a)
        if a isa AbstractArray
            E = eltype(a)
            E <: AbstractFloat && return E
            E <: Complex{<:AbstractFloat} && return real(E)
        end
        if a isa Tuple    # e.g. PropensityScoreConfig.trimming::Tuple{T,T}
            for t in a
                t isa AbstractFloat && return typeof(t)
            end
        end
    end
    return nothing
end

# Construct `T` from positional field values. Parametric types with an inner
# constructor expose only `T{P}(fields...)`; a struct with an un-parameterized
# inner constructor (e.g. `VARModel`) exposes only `T(fields...)`; a struct with
# no inner constructor has both. Try the parameterized form first, fall back.
function _generic_construct(T, args)
    if T isa UnionAll
        P = _infer_float_param(args)
        if P !== nothing
            try
                return T{P}(args...)
            catch e
                (e isa MethodError || e isa TypeError) || rethrow()
            end
        end
    end
    return T(args...)
end

# ─────────────────────────────────────────────────────────────────────────────
# Per-type reconstruction
# ─────────────────────────────────────────────────────────────────────────────

# Generic reconstruction: deserialize each public field and route through the
# type's real constructor (validation preserved). Forward-safe: a v1 loader
# knows exactly the v1 field set via `fieldnames(T)`.
function _from_serializable(::Type{T}, p::AbstractDict, ::Int) where {T}
    args = Any[_deser_field(p[String(f)]) for f in fieldnames(T)]
    return _generic_construct(T, args)
end

# ── explicit overrides where the constructor is not a plain positional call ──

function _from_serializable(::Type{VARModel}, p::AbstractDict, ::Int)
    VARModel(p["Y"], p["p"], p["B"], p["U"], p["Sigma"], p["aic"], p["bic"], p["hqic"], p["varnames"])
end

# `manifest` is a keyword-defaulted constructor argument, not a positional field.
function _from_serializable(::Type{BVARPosterior}, p::AbstractDict, ::Int)
    T = eltype(p["B_draws"])
    mani = _manifest_from_dict(get(p, "manifest", nothing))
    BVARPosterior{T}(p["B_draws"], p["Sigma_draws"], p["n_draws"], p["p"], p["n"],
                     p["data"], _as_symbol(p["prior"]), _as_symbol(p["sampler"]),
                     p["varnames"]; manifest=mani)
end

